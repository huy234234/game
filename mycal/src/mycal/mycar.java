    private void divideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divideActionPerformed
        // TODO add your handling code here:
        try {
            double num1 = Double.parseDouble(txta.getText());
            double num2 = Double.parseDouble(txtb.getText());

            if (num2 == 0) {
                // Handle division by zero
                txtc.setText("Division by zero is not allowed.");
            } else if(num1 ==0){
                if(num2 <0){
                    num2=-num2;
                    double chia = num1 / num2;
                    txtc.setText(chia + "");
                }
            }
            else {
                double chia = num1 / num2;
                txtc.setText(chia + "");
            }
        } catch (NumberFormatException e) {
            // Handle non-numeric input as before
            txtc.setText("Please enter numbers only.");
            System.err.println("Error: " + e.getMessage());
        }   
    }//GEN-LAST:event_divideActionPerformed

